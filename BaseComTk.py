# Importação de bibliotecas

from tkinter import *

# Inicio da inserção de classes





# Função MAIN
def main():
    janela = Tk()

    janela.title("Titulo da janela ") # personaliza o titulo da janela


    # Definição do Tamanho da janela
    janela.geometry("400x400+200+200")
